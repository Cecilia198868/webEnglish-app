import fs from "node:fs/promises";
import path from "node:path";
import OpenAI from "openai";

const LEVEL_IDS = ["everyday", "natural", "native"];
const BATCH_SIZE = 40;
const courseDataPath = path.join(process.cwd(), "data", "nativeFlow", "courseData.ts");
const outputPath = path.join(
  process.cwd(),
  "data",
  "nativeFlow",
  "chineseTranslations.ts",
);
const cachePath = path.join(
  process.cwd(),
  ".data",
  "native-flow-chinese-translations.json",
);

async function loadDotEnvLocal() {
  try {
    const text = await fs.readFile(path.join(process.cwd(), ".env.local"), "utf8");
    for (const line of text.split(/\r?\n/)) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith("#")) continue;

      const match = trimmed.match(/^([^=]+)=(.*)$/);
      if (!match) continue;

      const key = match[1].trim();
      const value = match[2].trim().replace(/^["']|["']$/g, "");
      if (!process.env[key]) {
        process.env[key] = value;
      }
    }
  } catch {}
}

function extractSentencesByLevel(source) {
  const match = source.match(
    /const nativeFlowSentencesByLevel: Record<NativeFlowLevelId, NativeFlowSentence\[]> = ([\s\S]*?);\n\nexport const nativeFlowProgressRows/,
  );

  if (!match) {
    throw new Error("Could not find nativeFlowSentencesByLevel in courseData.ts");
  }

  return JSON.parse(match[1]);
}

async function readCache() {
  try {
    const cached = JSON.parse(await fs.readFile(cachePath, "utf8"));
    return Object.fromEntries(
      LEVEL_IDS.map((levelId) => [
        levelId,
        Array.isArray(cached[levelId]) ? cached[levelId] : [],
      ]),
    );
  } catch {
    return Object.fromEntries(LEVEL_IDS.map((levelId) => [levelId, []]));
  }
}

async function writeCache(translationsByLevel) {
  await fs.mkdir(path.dirname(cachePath), { recursive: true });
  await fs.writeFile(
    cachePath,
    `${JSON.stringify(translationsByLevel, null, 2)}\n`,
    "utf8",
  );
}

function formatTs(translationsByLevel) {
  return `// Generated by scripts/generate-native-flow-chinese-translations.mjs.
// Static Simplified Chinese translations for the Native Flow course.

export const nativeFlowChineseTranslations = ${JSON.stringify(
    translationsByLevel,
    null,
    2,
  )} as const;
`;
}

async function translateBatch(openai, sentences) {
  const response = await openai.chat.completions.create({
    messages: [
      {
        role: "system",
        content:
          'Translate English learning-course sentences into natural Simplified Chinese. Keep the meaning, tone, and spoken feel. Return only strict JSON: {"translations":["..."]}. The translations array must have exactly the same length and order as the input.',
      },
      {
        role: "user",
        content: JSON.stringify({ sentences }),
      },
    ],
    model: process.env.OPENAI_TRANSLATION_MODEL || "gpt-4o-mini",
    response_format: { type: "json_object" },
    temperature: 0.2,
  });

  const content = response.choices[0]?.message?.content || "";
  const parsed = JSON.parse(content);
  const translations = parsed.translations;

  if (!Array.isArray(translations) || translations.length !== sentences.length) {
    throw new Error(
      `Translation count mismatch: expected ${sentences.length}, got ${translations?.length}`,
    );
  }

  return translations.map((translation) => String(translation).trim());
}

await loadDotEnvLocal();

if (!process.env.OPENAI_API_KEY) {
  throw new Error("OPENAI_API_KEY is required to generate translations.");
}

const source = await fs.readFile(courseDataPath, "utf8");
const sentencesByLevel = extractSentencesByLevel(source);
const translationsByLevel = await readCache();
const openai = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });

for (const levelId of LEVEL_IDS) {
  const sentences = sentencesByLevel[levelId].map((entry) => entry.english);
  translationsByLevel[levelId] = translationsByLevel[levelId].slice(0, sentences.length);

  for (let start = translationsByLevel[levelId].length; start < sentences.length; start += BATCH_SIZE) {
    const batch = sentences.slice(start, start + BATCH_SIZE);
    const translations = await translateBatch(openai, batch);
    translationsByLevel[levelId].push(...translations);
    await writeCache(translationsByLevel);
    console.log(
      `${levelId}: ${translationsByLevel[levelId].length}/${sentences.length}`,
    );
  }
}

await fs.writeFile(outputPath, formatTs(translationsByLevel), "utf8");
console.log(`Generated ${outputPath}`);
