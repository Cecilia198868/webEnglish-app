import fs from "node:fs/promises";
import path from "node:path";
import mammoth from "mammoth";

const TOTAL_DAYS = 30;
const DAILY_SENTENCES = 20;
const TOTAL_SENTENCES = TOTAL_DAYS * DAILY_SENTENCES;

const courseRoot = path.join(process.cwd(), "public", "images 2", "地道语感训练");
const outputFile = path.join(process.cwd(), "data", "nativeFlow", "courseData.ts");

const levels = [
  {
    badge: "初级",
    description: "从生活短句开始，建立语感基础",
    docOverrides: { 10: "A 10.docx" },
    englishTitle: "Everyday Flow",
    folder: "初级",
    id: "everyday",
    prefix: "A",
    title: "日常语感",
    tone: "green",
  },
  {
    badge: "中级",
    description: "更长句子，更自然的日常表达",
    docOverrides: { 29: "N29.docx" },
    englishTitle: "Natural Flow",
    folder: "中级",
    id: "natural",
    prefix: "B",
    title: "自然表达",
    tone: "blue",
  },
  {
    badge: "高级",
    description: "长句连读，高阶地道表达",
    englishTitle: "Native Flow",
    folder: "高级",
    id: "native",
    mp3Overrides: { 25: "c25.mp3" },
    prefix: "C",
    title: "地道语流",
    tone: "purple",
  },
];

function assetPath(...segments) {
  return `/${segments.map((segment) => encodeURIComponent(segment)).join("/")}`;
}

function normalizeSentence(line) {
  return line
    .replace(/\u00a0/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/^\.+\s*/, "")
    .trim();
}

async function listSourceDays(level) {
  const dir = path.join(courseRoot, level.folder);
  const names = await fs.readdir(dir);
  return names
    .filter((name) => name.toLowerCase().endsWith(".docx"))
    .map((name) => {
      const match = name.match(/(\d+)/);
      return match ? { day: Number(match[1]), name } : null;
    })
    .filter(Boolean)
    .sort((a, b) => a.day - b.day);
}

async function extractEnglishLines(filePath) {
  const result = await mammoth.extractRawText({ path: filePath });
  return result.value
    .split(/\r?\n/)
    .map(normalizeSentence)
    .filter(Boolean);
}

function formatTs(data) {
  return `// Generated by scripts/generate-native-flow-data.mjs from the official course files.
// Source: public/images 2/地道语感训练/{初级,中级,高级}/*.docx and *.mp3

export type NativeFlowLevelId = "everyday" | "natural" | "native";

export type NativeFlowTone = "green" | "blue" | "purple";

export type NativeFlowSentence = {
  audioSrc: string;
  chinese: string;
  day: number;
  daySentence: number;
  english: string;
  id: number;
  sourceAudio: string;
  sourceDocument: string;
};

export type NativeFlowLevel = {
  badge: string;
  description: string;
  englishTitle: string;
  id: NativeFlowLevelId;
  title: string;
  tone: NativeFlowTone;
  totalDays: number;
  totalSentences: number;
  dailySentences: number;
};

export type NativeFlowProgressRow = {
  completed: number;
  levelId: NativeFlowLevelId;
  percent: number;
};

export const NATIVE_FLOW_TOTAL_DAYS = ${TOTAL_DAYS};
export const NATIVE_FLOW_DAILY_SENTENCES = ${DAILY_SENTENCES};
export const NATIVE_FLOW_TOTAL_SENTENCES =
  NATIVE_FLOW_TOTAL_DAYS * NATIVE_FLOW_DAILY_SENTENCES;

export const nativeFlowLevels: NativeFlowLevel[] = ${JSON.stringify(data.levels, null, 2)};

export const nativeFlowLevelIds = nativeFlowLevels.map((level) => level.id);

const nativeFlowSentencesByLevel: Record<NativeFlowLevelId, NativeFlowSentence[]> = ${JSON.stringify(data.sentencesByLevel, null, 2)};

export const nativeFlowProgressRows: NativeFlowProgressRow[] = nativeFlowLevels.map((level) => ({
  completed: 0,
  levelId: level.id,
  percent: 0,
}));

export const nativeFlowCourseStats = ${JSON.stringify(data.stats, null, 2)};

export function getNativeFlowLevel(levelId: string) {
  return nativeFlowLevels.find((level) => level.id === levelId);
}

export function getNativeFlowSentence(levelId: string, sentenceId: number) {
  const level = getNativeFlowLevel(levelId);
  if (!level) return null;

  const normalizedSentenceId = Number.isFinite(sentenceId)
    ? Math.min(Math.max(Math.floor(sentenceId), 1), level.totalSentences)
    : 1;
  const sentence = nativeFlowSentencesByLevel[level.id][normalizedSentenceId - 1];

  return sentence ? { level, sentence } : null;
}
`;
}

const data = {
  levels: [],
  sentencesByLevel: {},
  stats: {
    generatedAt: new Date().toISOString(),
    sourceRoot: "public/images 2/地道语感训练",
    levels: {},
    translationFilesFound: false,
  },
};

for (const level of levels) {
  const sourceDays = await listSourceDays(level);
  const entries = [];

  for (const sourceDay of sourceDays) {
    const docName =
      level.docOverrides?.[sourceDay.day] || `${level.prefix}${sourceDay.day}.docx`;
    if (docName !== sourceDay.name && !sourceDays.some((item) => item.name === docName)) {
      continue;
    }

    const actualDocName = sourceDays.some((item) => item.name === docName)
      ? docName
      : sourceDay.name;
    const mp3Name =
      level.mp3Overrides?.[sourceDay.day] || `${level.prefix}${sourceDay.day}.mp3`;
    const lines = await extractEnglishLines(path.join(courseRoot, level.folder, actualDocName));
    const selectedLines = lines.slice(0, DAILY_SENTENCES);

    for (const english of selectedLines) {
      entries.push({
        audioSrc: assetPath("images 2", "地道语感训练", level.folder, mp3Name),
        chinese: "",
        english,
        sourceAudio: `${level.folder}/${mp3Name}`,
        sourceDocument: `${level.folder}/${actualDocName}`,
      });
    }
  }

  if (entries.length < TOTAL_SENTENCES) {
    throw new Error(
      `${level.title} only has ${entries.length} sentences; expected ${TOTAL_SENTENCES}.`,
    );
  }

  const sentences = entries.slice(0, TOTAL_SENTENCES).map((entry, index) => ({
    ...entry,
    day: Math.floor(index / DAILY_SENTENCES) + 1,
    daySentence: (index % DAILY_SENTENCES) + 1,
    id: index + 1,
  }));

  data.levels.push({
    badge: level.badge,
    dailySentences: DAILY_SENTENCES,
    description: level.description,
    englishTitle: level.englishTitle,
    id: level.id,
    title: level.title,
    tone: level.tone,
    totalDays: TOTAL_DAYS,
    totalSentences: TOTAL_SENTENCES,
  });

  data.sentencesByLevel[level.id] = sentences;
  data.stats.levels[level.id] = {
    docxFilesFound: sourceDays.length,
    generatedSentences: sentences.length,
    mp3FilesUsed: new Set(sentences.map((sentence) => sentence.sourceAudio)).size,
    sourceFolder: `public/images 2/地道语感训练/${level.folder}`,
    sourceLinesRead: entries.length,
  };
}

await fs.writeFile(outputFile, formatTs(data), "utf8");
console.log(`Generated ${outputFile}`);
